    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            String name = tf_name.getText();
            String goods = tf_goods.getText();
            String price = tf_price.getText();
            String stock = tf_stock.getText();
            String category = cb_category.getSelectedItem().toString();
            Date date = dp_income.getDate();
            
            
            
            model.setName(name);
            model.setGoods(goods);
            model.setPrice(price);
            model.setCategory(category);
            model.setIncome(date);
            model.setStock(stock);
                        
            Boolean result = controller.update(product_id, model);
            if (result) {
                JOptionPane.showMessageDialog(null, "Update Success!");                
            }
            else {
                JOptionPane.showMessageDialog(null, "Update Failed!");
            }
            refresh();
            getAllData();
            dp_income.setEnabled(true);
        } 
        catch (Exception ex) {
            System.err.println(ex.getMessage());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

