    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        try {
            String name = tf_name.getText();
            String goods = tf_goods.getText();
            String price = tf_price.getText();
            String stock = tf_stock.getText();
            String category = cb_category.getSelectedItem().toString();
            Date date = dp_income.getDate();
            
            
            
            
            model.setName(name);
            model.setGoods(goods);
            model.setPrice(price);
            model.setCategory(category);
            model.setIncome(date);
            model.setStock(stock);
            
            
            Boolean result = controller.create(model);
            Boolean check_name = logic.checkName(name);
            if (!check_name) {
               if(result) {
                JOptionPane.showMessageDialog(null, "Berhasil Menambahkan Data");
                refresh();
            }
            else {
                JOptionPane.showMessageDialog(null, 
                        "Please input character only for your name!");
            }
            getAllData(); 
            }
            
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btn_submitActionPerformed

